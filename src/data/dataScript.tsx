const getSeasonFromVideoName = (name: string) => {
  const blah = name.split("Season ");
  return blah[1].substring(0, 1);
};

const getTeamNameFromVideoName = (name: string) => {
  const secondBit = name.split(" - ")[1];
  return secondBit;
};

const getVideoUrlFromVideoName = (href: string, name: string) => {
  const thirdBit = name.split(" - ")[2] || "Link";
  return { href, name: thirdBit };
};

const data = elements.map((ele, index) => {
  if (Array.isArray(ele)) {
    return {
      id: index,
      season: parseInt(getSeasonFromVideoName(ele[0].textContent), 10),
      teamName: getTeamNameFromVideoName(ele[0].textContent),
      videoUrls: ele.map((e) => {
        return getVideoUrlFromVideoName(e.href, e.textContent);
      }),
    };
  }

  return {
    id: index,
    season: parseInt(getSeasonFromVideoName(ele.textContent), 10),
    teamName: getTeamNameFromVideoName(ele.textContent),
    videoUrls: [getVideoUrlFromVideoName(ele.href, ele.textContent)],
  };
});

export default data;
